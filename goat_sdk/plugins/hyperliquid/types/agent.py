"""Types for Hyperliquid agent wallet functionality."""

from pydantic import BaseModel, Field, field_validator
from typing import Optional, Dict, Any, Literal
import re

class AgentApprovalRequest(BaseModel):
    """Request model for approving an agent wallet."""
    agent_address: str = Field(..., description="42-character hex address of the agent wallet")
    agent_name: Optional[str] = Field(None, description="Optional name for the agent wallet")
    chain_id: str = Field("0xa4b1", description="Chain ID in hex format (default: Arbitrum)")
    
    @field_validator("agent_address")
    def validate_address(cls, v: str) -> str:
        if not re.match(r"^0x[a-fA-F0-9]{40}$", v):
            raise ValueError("Invalid Ethereum address format")
        return v.lower()
    
    class Config:
        frozen = True

class AgentApprovalAction(BaseModel):
    """Action model for agent approval request."""
    type: Literal["approveAgent"] = "approveAgent"
    hyperliquidChain: str
    signatureChainId: str
    agentAddress: str
    agentName: Optional[str] = None
    time: int
    nonce: int

class AgentApprovalResponse(BaseModel):
    """Response model for agent approval request."""
    status: str
    response: Dict[str, Any]