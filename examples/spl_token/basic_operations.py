"""
Basic SPL Token operations example.

This example demonstrates common operations with SPL tokens:
- Getting token balance
- Transferring tokens
- Checking account existence
"""

import asyncio
from goat_sdk import GoatSDK
from goat_sdk.core.types import Network, Chain
from goat_sdk.plugins.spl_token import SplTokenPlugin

async def main():
    # Initialize SDK with your private key
    sdk = GoatSDK(
        private_key="your_private_key",  # Replace with your private key
        network=Network.MAINNET,
        chain=Chain.SOLANA
    )

    # Initialize SPL Token plugin
    spl = SplTokenPlugin(sdk)

    # Example USDC mint address
    usdc_mint = "EPjFWdd5AufqSSqeM2qN1xzybapC8G4wEGGkZwyTDt1v"
    
    # Get token balance
    balance = await spl.get_token_balance(
        mint_address=usdc_mint,
        owner_address="your_wallet_address"  # Replace with your wallet address
    )
    print(f"USDC Balance: {balance}")

    # Check if an account exists
    account_exists = await spl.check_token_account_exists(
        mint_address=usdc_mint,
        owner_address="recipient_address"  # Replace with recipient address
    )
    print(f"Account exists: {account_exists}")

    if account_exists:
        # Transfer tokens
        result = await spl.transfer_token(
            mint_address=usdc_mint,
            to_address="recipient_address",  # Replace with recipient address
            amount=1000000  # 1 USDC (6 decimals)
        )
        print(f"Transfer result: {result}")

if __name__ == "__main__":
    asyncio.run(main()) 